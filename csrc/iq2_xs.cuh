// ============================================================================
// 第一部分: 常量
// ============================================================================

// 超级块大小 — 每个 block_iq2_xs 编码 256 个元素
#define QK_K           256

// 量化比率与索引数
// IQ2_XS: 每个 uint16_t 的低 9-bit 编码 grid 索引 (0-511), 高 7-bit 编码符号索引
// QR2_XS = 4: 每个 uint16_t 解码为 4 个 int32 (8 个 int8), 即 32 个元素
#define QR2_XS         4
#define QI2_XS         (QK_K / (4 * QR2_XS))  // = 16

// Q8_0 / Q8_1 block 大小常量 (GEMM 的 B 矩阵格式)
#define QK8_0          32
#define QR8_0          1
#define QI8_0          (QK8_0 / (4 * QR8_0))  // = 8

#define QK8_1          32
#define QR8_1          1
#define QI8_1          (QK8_1 / (4 * QR8_1))  // = 8

// VDR (Vector Dot Ratio) — 点积分组比率
// MMVQ: 每个线程一次处理 VDR 个连续整数组
// MMQ:  每个线程一次处理 VDR 个连续整数组 (通常与 MMVQ 不同)
#define VDR_IQ2_XS_Q8_1_MMVQ  2
#define VDR_IQ2_XS_Q8_1_MMQ   2

// ============================================================================
// 第二部分: MMQ tile 常量
// 来源: ggml/src/ggml-cuda/mmq.cuh
// ============================================================================

#define MMQ_ITER_K             256   // 每次迭代处理的 K 维度
#define MMQ_NWARPS               8   // MMQ 使用的 warp 数量
#define MMQ_TILE_NE_K           32   // 每个 tile 的 K 元素数 (32 bit 元素)

// DP4A 路径的 shared memory 布局大小
// tile_x_sizes { qs, dm, sc }
// IQ2_XS 使用 Q8_0_16 布局
#define MMQ_DP4A_TXS_Q8_0_16_QS(mmq_y)  ((mmq_y) * MMQ_TILE_NE_K * 2 + (mmq_y))
#define MMQ_DP4A_TXS_Q8_0_16_DS(mmq_y)  ((mmq_y) * MMQ_TILE_NE_K * 4 / QI8_0 + (mmq_y) / (QI8_0 / 4))

// MMA 路径的 tile X_K 大小
// IQ2_XS 使用 Q3_K 布局: 2*MMQ_TILE_NE_K + MMQ_TILE_NE_K/2 + 4 = 84
#define MMQ_MMA_TILE_X_K_Q3_K  (2 * MMQ_TILE_NE_K + MMQ_TILE_NE_K / 2 + 4)

// block_q8_1_mmq 的 Y tile 大小
// 128 个 8-bit int = 32 个 32-bit int + 4 个 32-bit scale
#define MMQ_TILE_Y_K     (MMQ_TILE_NE_K + MMQ_TILE_NE_K / QI8_1)

// ============================================================================
// 第三部分: 数据结构
// 来源: ggml/src/ggml-common.h
// ============================================================================

// IQ2_XS 量化块: 2.3125 bpw
// 每个 block 编码 256 个元素
// d:     FP16 全局缩放因子
// qs:    32 个 uint16_t, 低 9-bit = grid 索引, 高 7-bit = 符号索引
// scales: 8 个 uint8_t, 高低 4-bit 各编码一个子块缩放
typedef struct {
    __half   d;              // FP16 全局缩放因子 (与 ggml_half 二进制兼容)
    uint16_t qs[QK_K/8];    // 32 个 uint16_t
    uint8_t  scales[QK_K/32]; // 8 个 uint8_t
} block_iq2_xs;

// Q8_0 量化块: 8.5 bpw (用于 GEMM B 矩阵)
typedef struct {
    __half   d;             // FP16 delta (与 ggml_half 二进制兼容)
    int8_t   qs[QK8_0];    // 32 个 int8 量化值
} block_q8_0;

// Q8_1 量化块: 8.5 bpw (用于 GEMM B 矩阵, 带 partial sum)
// 来源: ggml/src/ggml-common.h — 使用 half2 打包 d 和 s
// ds.x = d (delta), ds.y = s (d * sum(qs[i]))
typedef struct {
    half2    ds;            // half2 打包: 低 16-bit = d (delta), 高 16-bit = s (d * sum(qs[i]))
    int8_t   qs[QK8_1];    // 32 个 int8 量化值
} block_q8_1;

// Q8_1 MMQ 格式 — 4 个子块, 每个 32 个 int8
// 用于 MMQ (矩阵-矩阵量化乘法) 的 B 矩阵布局
// 数据按 128 值分组, 转置后拷贝到 shared memory
// 为避免 bank conflict, 每个 block 填充 16 字节 (用于存储 scale/partial sum)
typedef struct {
    // DS 布局取决于 x 数据类型, IQ2_XS 使用 D4 布局
    union {
        float d4[4];    // D4: 1 个 32-bit scale / 32 值, 存储为 d0,d1,d2,d3
        uint32_t ds4[4]; // DS4: 1 个 16-bit scale + 1 个 16-bit partial sum / 32 值
        uint16_t d2s6[8]; // D2S6: 1 个 16-bit scale / 64 值 + 1 个 16-bit partial sum / 16 值
    };
    int8_t qs[4 * QK8_1]; // 128 个 int8 量化值
} block_q8_1_mmq;

