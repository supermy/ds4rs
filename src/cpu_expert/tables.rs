/// IQ2_XXS 预计算符号掩码表：128 entries × 8 i8 = 1024 bytes
///
/// 将 KSIGNS_IQ2XS 的 bit mask 转换为 _mm_sign_epi8 可直接使用的 i8 掩码：
///   bit j = 1 → -1 (取反), bit j = 0 → +1 (保持)
///
/// 避免内核中逐 bit 构造符号掩码的开销（每组 8 次条件判断 → 1 次表查找）
pub const KSIGNS_IQ2XS_MASKS: [[i8; 8]; 128] = {
    let mut table = [[0i8; 8]; 128];
    let mut i = 0;
    while i < 128 {
        let byte = KSIGNS_IQ2XS[i];
        let mut j = 0;
        while j < 8 {
            table[i][j] = if byte & (1 << j) != 0 { -1i8 } else { 1i8 };
            j += 1;
        }
        i += 1;
    }
    table
};

/// 获取 ksigns_iq2xs 字节表指针，供 AVX-512 内核使用
pub fn get_ksigns_iq2xs_bytes() -> *const u8 {
    KSIGNS_IQ2XS.as_ptr()
}

/// IQ2_XXS 预计算 sign_256 查找表：128 entries × 8 bytes = 1KB
///
/// 将 KSIGNS_IQ2XS_MASKS[i] 的 8 个 i8 值重解释为 u64，供 _mm256_set_epi64x 直接使用。
/// 内核中 sign_256 = _mm256_set_epi64x(s[3], s[2], s[1], s[0])，
/// 其中 s[k] = sign_u64_table[sign_idx[k]]。
///
/// 替代内核中的 *(sign_masks[idx].as_ptr() as *const i64) 标量查表。
/// 1KB 完全常驻 L1 缓存。
pub const KSIGNS_IQ2XS_U64: [u64; 128] = {
    let mut table = [0u64; 128];
    let mut i = 0;
    while i < 128 {
        let b0 = KSIGNS_IQ2XS_MASKS[i][0] as u8;
        let b1 = KSIGNS_IQ2XS_MASKS[i][1] as u8;
        let b2 = KSIGNS_IQ2XS_MASKS[i][2] as u8;
        let b3 = KSIGNS_IQ2XS_MASKS[i][3] as u8;
        let b4 = KSIGNS_IQ2XS_MASKS[i][4] as u8;
        let b5 = KSIGNS_IQ2XS_MASKS[i][5] as u8;
        let b6 = KSIGNS_IQ2XS_MASKS[i][6] as u8;
        let b7 = KSIGNS_IQ2XS_MASKS[i][7] as u8;
        table[i] = (b0 as u64) | ((b1 as u64) << 8) | ((b2 as u64) << 16) | ((b3 as u64) << 24)
                 | ((b4 as u64) << 32) | ((b5 as u64) << 40) | ((b6 as u64) << 48) | ((b7 as u64) << 56);
        i += 1;
    }
    table
};

pub fn get_sign_mul_table() -> &'static [f32] {
    SIGN_MUL_TABLE.get_or_init(|| vec![1.0f32; 128 * 8])
}

pub fn get_scale_decode_table() -> &'static [f32] {
    SCALE_DECODE_TABLE.get_or_init(|| {
        let mut table = vec![0.0f32; 256 * 2];
        for i in 0..256 {
            table[i * 2] = 2.0 * (i & 0xF) as f32 + 1.0;
            table[i * 2 + 1] = 2.0 * ((i >> 4) & 0xF) as f32 + 1.0;
        }
        table
    })
}

pub fn get_sign_i8_table() -> &'static [i8] {
    SIGN_I8_TABLE.get_or_init(|| vec![1i8; 128 * 8])
}

pub fn init_tables(grid_u64: Vec<u64>, ksigns: Vec<u8>) {
    let mut grid_table = vec![0i8; 512 * 8];
    for (i, &g) in grid_u64.iter().enumerate() {
        let bytes = g.to_le_bytes();
        for (j, &b) in bytes.iter().enumerate() {
            grid_table[i * 8 + j] = b as i8;
        }
    }

    // 预计算 grid_u64 打包表（避免每次点积调用时重建）
    let grid_u64_packed = grid_u64.clone();

    let mut sign_table = vec![0.0f32; 128 * 8];
    let mut sign_i8 = vec![1i8; 128 * 8];
    for (i, &k) in ksigns.iter().enumerate() {
        for j in 0..8 {
            let is_neg = (k >> j) & 1 == 1;
            sign_table[i * 8 + j] = if is_neg { -1.0f32 } else { 1.0f32 };
            sign_i8[i * 8 + j] = if is_neg { -1i8 } else { 1i8 };
        }
    }

    // 注意：signed_grid 表已移除（512KB），当前 maddubs+madd 路径不需要

    let _ = IQ2XS_GRID.set(grid_table).map_err(|_| {
        eprintln!("[cpu_expert] WARNING: IQ2XS_GRID already initialized, ignoring init_tables() call");
    });
    let _ = IQ2XS_GRID_U64.set(grid_u64_packed).map_err(|_| {
        eprintln!("[cpu_expert] WARNING: IQ2XS_GRID_U64 already initialized, ignoring init_tables() call");
    });
    let _ = SIGN_MUL_TABLE.set(sign_table).map_err(|_| {
        eprintln!("[cpu_expert] WARNING: SIGN_MUL_TABLE already initialized, ignoring init_tables() call");
    });
    let _ = SIGN_I8_TABLE.set(sign_i8).map_err(|_| {
        eprintln!("[cpu_expert] WARNING: SIGN_I8_TABLE already initialized, ignoring init_tables() call");
    });
}

pub fn is_tables_initialized() -> bool {
    IQ2XS_GRID.get().is_some() && SIGN_MUL_TABLE.get().is_some()
}
