/// 获取预计算的 grid_u64 表，避免每次点积调用时重建
pub fn get_iq2xs_grid_u64() -> &'static [u64] {
    IQ2XS_GRID_U64.get_or_init(|| {
        let grid = get_iq2xs_grid();
        let mut grid_u64 = vec![0u64; 512];
        for i in 0..512 {
            let mut packed = 0u64;
            for j in 0..8 {
                packed |= (grid[i * 8 + j] as u8 as u64) << (j * 8);
            }
            grid_u64[i] = packed;
        }
        grid_u64
    })
}

